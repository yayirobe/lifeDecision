        <h2 class="text-uppercase">Quienes <span class="font-weight-light">somos</span></h2>
        Dentro del Ministerio Life Decision, se dió apertura a un grupo encargado de la capacitación y establecimiento de la Lengua de Señas como herramienta para llevar el evangelio completo de Jesucristo a la comunidad sorda de la ciudad de Rosario. 
        La Biblia muy claramente dice que “la Fe es por el oír, y el oír, por la palabra de Dios” (Romanos 10:17). Esto nos llevó a preguntarnos: ¿cómo el mundo de los sordos puede acceder a la Fe, y por ende a la salvación de sus vidas? Dicho versículo originó en nosotros un despertar al compromiso genuino basado en la necesidad dada por la condición de las personas sordas al no poder acceder a la palabra de Dios como cualquier oyente. He aquí el fundamento de la gran importancia que conlleva este ministerio. 

        El año 2018 fue un año bisagra donde se estableció la visión y los objetivos claros para llevar a cabo un coordinado desempeño ministerial con el fin de alcanzar los desafíos propuestos por Dios. 
        Después de haber atravesado muchos meses de búsqueda de identidad en oración, se establece el nombre “MANOS DE VIDA” para dicho ministerio, donde actualmente se continúa trabajando bajo la cobertura de la visión establecida para la casa.
        Proverbios 18:21 da a conocer cómo en nuestra boca está el poder dar vida y también dar muerte, siendo nuestra vía directa de comunicación. Ahora bien, a través de esta palabra el Señor nos permite llevar este concepto a la vía de comunicación directa de las personas sordas que son sus manos. Por ende, el nombre de este ministerio es lo que literalmente expresa: Manos que dan vida.
